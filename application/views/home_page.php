<script >
  
  $(document).ready(function () { 
    
    $.ajax({
      url: '<?php echo base_url(); ?>index.php/home_controller/get_stats',
        type: 'POST',  
    })
    .done(function(data) {
      
      var output = JSON.parse(data); 
      console.log(output);

      $('#total_open_orders').html(output.total_open_orders)
      $('#completed_orders').html(output.completed_orders)
      $('#cancelled_orders').html(output.cancelled_orders)
      $('#estimated_orders').html(output.estimated_orders)
      $('#total_orders').html(output.total_orders)
      $('#this_month_earnings').html(output.this_month_earnings)

      var pie_data = [output.total_open_orders, output.completed_orders, output.estimated_orders];

      pieChart(pie_data);

      var months = output.get_earnings.months;
      var total_sales = output.get_earnings.total_sales;

      barChart(months, total_sales)
   

    })
    .fail(function() {
      console.log("error");
    })
    .always(function() {
      console.log("complete");
    });

    function pieChart(data){
      var ctx = document.getElementById("myPieChart");
      var myPieChart = new Chart(ctx, {
        type: 'doughnut',
        data: {
          labels: ["Open", "Completed", "Estimated"],
          datasets: [{
            data: data,
            backgroundColor: ['#1cc88a', '#4e73df', '#f6c23e'], 
            hoverBorderColor: "rgba(234, 236, 244, 1)",
          }],
        },
        options: {
          maintainAspectRatio: false,
          tooltips: {
            backgroundColor: "rgb(255,255,255)",
            bodyFontColor: "#858796",
            borderColor: '#dddfeb',
            borderWidth: 1,
            xPadding: 15,
            yPadding: 15,
            displayColors: false,
            caretPadding: 10,
          },
          legend: {
            display: false
          },
          cutoutPercentage: 80,
        },
      });
    }

  });


  function barChart(months, total_sales){
    // Area Chart Example
    var ctx = document.getElementById("myAreaChart");
    var myLineChart = new Chart(ctx, {
      type: 'bar',
      data: {
        labels: months,
        datasets: [{
          label: "Earnings",
          lineTension: 0.3,
          backgroundColor: "#4e73df",
          borderColor: "rgba(78, 115, 223, 1)",
          pointRadius: 3,
          pointBackgroundColor: "rgba(78, 115, 223, 1)",
          pointBorderColor: "rgba(78, 115, 223, 1)",
          pointHoverRadius: 3,
          pointHoverBackgroundColor: "rgba(78, 115, 223, 1)",
          pointHoverBorderColor: "rgba(78, 115, 223, 1)",
          pointHitRadius: 10,
          pointBorderWidth: 1,
          data: total_sales,
        }],
      },
      options: {
        maintainAspectRatio: false,
        layout: {
          padding: {
            left: 10,
            right: 25,
            top: 25,
            bottom: 0
          }
        },
        scales: {
          xAxes: [{
            time: {
              unit: 'date'
            },
            gridLines: {
              display: false,
              drawBorder: false
            },
            ticks: {
              maxTicksLimit: 7
            }
          }],
          yAxes: [{
            ticks: {
              maxTicksLimit: 5,
              padding: 10,
              // Include a dollar sign in the ticks
              callback: function(value, index, values) {
                return 'Rs.' + number_format(value);
              }
            },
            gridLines: {
              color: "rgb(234, 236, 244)",
              zeroLineColor: "rgb(234, 236, 244)",
              drawBorder: false,
              borderDash: [2],
              zeroLineBorderDash: [2]
            }
          }],
        },
        legend: {
          display: false
        },
        tooltips: {
          backgroundColor: "rgb(255,255,255)",
          bodyFontColor: "#858796",
          titleMarginBottom: 10,
          titleFontColor: '#6e707e',
          titleFontSize: 14,
          borderColor: '#dddfeb',
          borderWidth: 1,
          xPadding: 15,
          yPadding: 15,
          displayColors: false,
          intersect: false,
          mode: 'index',
          caretPadding: 10,
          callbacks: {
            label: function(tooltipItem, chart) {
              var datasetLabel = chart.datasets[tooltipItem.datasetIndex].label || '';
              return datasetLabel + ': Rs.' + number_format(tooltipItem.yLabel);
            }
          }
        }
      }
    });
  }
